function applyLanguage(lang) {
    // Always force VI
    lang = 'vi';
    document.documentElement.lang = 'vi';

    // Update Page Title
    const fileName = window.location.pathname.split('/').pop() || 'index.html';
    const titleKey = pageTitleKeys[fileName];
    if (titleKey && translations[lang] && translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }

    // Update text for all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                // FIX for HTML entities (like <br>) and preventing duplication
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Hide any language switcher buttons
    const langBtn = document.getElementById('lang-switch-btn');
    if (langBtn) langBtn.style.display = 'none';
}

