// Global Function to Change Language
window.changeLanguage = function (langCode) {
    // 1. Show "Translating..." Notification
    showTransNotification(langCode === 'vi' ? 'Đang khôi phục ngôn ngữ...' : 'Switching language...', 'loading');

    // 2. Simulate small delay for effect
    setTimeout(() => {
        applyLanguage(langCode);

        // 3. Show "Translated" Notification
        showTransNotification(langCode === 'vi' ? 'Đã chuyển về Tiếng Việt' : 'Language switched to English', 'success');

        // 4. Update Selectors
        updateSelectors(langCode);
    }, 500);
};

function updateSelectors(lang) {
    const selectors = document.querySelectorAll('#custom-lang-selector');
    selectors.forEach(sel => sel.value = lang);
}

function showTransNotification(msg, type) {
    // Check if notification container exists
    let container = document.getElementById('trans-notify-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'trans-notify-container';
        container.style.cssText = `
            position: fixed;
            top: 20px;
            right: 20px;
            z-index: 99999;
            font-family: 'Roboto', sans-serif;
        `;
        document.body.appendChild(container); // Safe to append to body
    }

    const toast = document.createElement('div');
    const bgColor = type === 'loading' ? '#2196F3' : '#4CAF50';
    const icon = type === 'loading' ? '<i class="fa-solid fa-spinner fa-spin"></i>' : '<i class="fa-solid fa-check"></i>';

    toast.innerHTML = `${icon} <span style="margin-left:8px">${msg}</span>`;
    toast.style.cssText = `
        background-color: ${bgColor};
        color: white;
        padding: 12px 24px;
        margin-bottom: 10px;
        border-radius: 4px;
        box-shadow: 0 4px 6px rgba(0,0,0,0.1);
        display: flex;
        align-items: center;
        opacity: 0;
        transform: translateY(-20px);
        transition: all 0.3s ease;
        min-width: 200px;
    `;

    container.appendChild(toast);

    // Animate In
    requestAnimationFrame(() => {
        toast.style.opacity = '1';
        toast.style.transform = 'translateY(0)';
    });

    // Remove after 3s
    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transform = 'translateY(-20px)';
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}


